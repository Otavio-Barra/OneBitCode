const numberA = Number(prompt("insira um numero"));
const numberB = Number(prompt("insira outro numero"));

alert(
  `${numberA} + ${numberB} = ${numberA + numberB}
  ${numberA} - ${numberB} = ${numberA - numberB}
  ${numberA} * ${numberB} = ${numberA * numberB}
  ${numberA} / ${numberB} = ${numberA / numberB}
  ${numberA} % ${numberB} = ${numberA % numberB}`
);
